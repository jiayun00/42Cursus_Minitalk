#include "ft_printf.h"

int	ft_print_str(char *str)
{
	int	i;

	if (str == NULL)
	{
		return (ft_print_str("(null)"));
	}
	i = 0;
	while (str[i])
	{
		ft_print_chr(str[i]);
		i++;
	}
	return (i);
}
